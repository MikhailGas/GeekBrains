1. В чем заключается суть ключевого слова global? Когда его применение целесообразно?
Global обозначает глобальную область видимости переменных. Если объявить global $variable, то функция будет ссылаться на глобальные переменные.
Применение Global необходимо, когда нужно логика внутри функции должна изменить глобальную переменную.
 
