4. Что выведет программа в каждом случае и почему?



function changeX($x){
   $x += 5;
   echo $x; // 6
}
$x = 1;
echo $x; // 1
changeX($x);
echo $x; // 1

В строке 33 выведет "1", так как $x присвоили 1.
Далее вызов функции changeX с аргументом $x = 1.
Внутри функции к $x прибавится 5 в строке 29 и на экран выведется 6.
Так как переменная $x внутри функции является локальной, то $x глобальная останется равной 1 и в строке 35 на экран веведется 1.


